var bounceAmplitude:float = 0.05;
var bounceFreq:float = 2.5;

///////////////////////////////////////////////////////////
// HELPERS
///////////////////////////////////////////////////////////

function NoBounce() {
	Destroy(this);
}


///////////////////////////////////////////////////////////
// UNITY FUNCTIONS
///////////////////////////////////////////////////////////

function Update() {
	transform.position.y += bounceAmplitude * Mathf.Cos(bounceFreq * Time.fixedTime);
}